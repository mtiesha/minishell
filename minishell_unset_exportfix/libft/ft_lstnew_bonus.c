#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*p;

	p = (t_list *)malloc(1 * sizeof(t_list));
	if (p != NULL)
	{
		if (!content)
			p -> content = NULL;
		else
			p -> content = content;
		p -> next = NULL;
		return (p);
	}
	return (NULL);
}
