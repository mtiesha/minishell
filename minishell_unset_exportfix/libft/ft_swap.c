#include "libft.h"

/*
    ft_swap_ptr - take &ptr
*/

void	ft_swap_ptr(char **p1, char **p2)
{
	char	*tmp;

	tmp = (*p1);
	(*p1) = (*p2);
	(*p2) = tmp;
}
