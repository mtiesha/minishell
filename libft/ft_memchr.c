#include "libft.h"

void	*ft_memchr(const void *src, int c, size_t n)
{
	unsigned char	*str;

	str = (unsigned char *)(src);
	while (n)
	{
		if (*str == (unsigned char)(c))
		{
			src = str;
			return ((char *)(src));
		}
		str++;
		n--;
	}
	return (NULL);
}
